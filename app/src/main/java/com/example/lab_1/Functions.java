package com.example.lab_1;

public class Functions {
    public int minFunc(int a, int b){
        if(a < b){
            return a;
        }else{
            return b;
        }
    }

    public int maxFunc(int a, int b){
        if(a > b){
            return a;
        }else{
            return b;
        }
    }
}
